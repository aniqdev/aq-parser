function fix_it($ebay_id)
{
	$specifics = parse_item_specifics($ebay_id);
	unset($specifics['Artikelzustand']);
	unset($specifics['EAN']);

	foreach ($specifics as $key => $value) {
		if (strlen($value) > 65) {
			if ($key !== 'Plattform' && $key !== 'Genre') {
				$specifics[$key] = explode(',', $value);
			}else{
				$specifics[$key] = preg_replace('/(.{1,65}),.*/', '$1', $value);
			}			
		}
	}
	
	$output = '<pre>'.print_r($specifics,1).'</pre>';

	$ebayObj = new Ebay_shopping2();
	$res = $ebayObj->UpdateCategorySpecifics($ebay_id, $specifics);

	unset($res['Fees']);
	echo $output,'<pre>'.print_r($res,1).'</pre><hr>';
}


